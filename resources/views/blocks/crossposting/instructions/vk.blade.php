<p>Для того, чтобы подключить ВК:</p>
<ol>
    <li>Нажмите кнопку "Подключить"</li>
    <li>Разрешите приложению доступ</li>
    <li>Вас перенаправит на страницу с URL вида "https://oauth.vk.com/blank.html#access_token=<strong>token</strong>&expires_in=0&user_id=0"</li>
    <li>Скопируйте этот токен и вставьте в поле "Access token"</li>
</ol>